                            <h4>3. Pedagogy: -</h4>
                            <p class="text-justify">In the modern era of technology, remote sensing (RS) and geographic information systems (GIS) play an important role in diverse fields including water resources, geography, geology, environment, urban planning, hydrology, ecology, forest management, etc. In the course will learn the basics of geographic information systems, GIS software available, download of QGIS and its installation, geographic projection system, import of hard sheets and toposheets, georeferencing of external sheets, digitization and preparation of layout maps. In the course, participants will learn about various freely available data available online for various applications. Remote sensing data provide important input due to their synoptic viewing of large areas at regular intervals and this course will give insight into remote sensing principles, data source and type of data, and their possible application in land use classification. The course is designed in such a way that every lecture will be followed by practical sessions and tutorials for great learning of these techniques. It is expected that participants have a laptop or computer with them to carry out tutorials. The faculties for the course will be scientists from the National Institute of Hydrology, Bhopal and Roorkee, National Remote Sensing Centre, Hyderabad, professors from Maulana Azad National Institute of Technology, Indira Gandhi Krishi Vishvidalaya Raipur, and Engineers/Professional working with Multi-National Companies. The course lectures will be organized in the evening session online so working officers, students, and professionals can join the course without disturbing their daily routine. At the end of the session, a test will be conducted and a certificate will be awarded after successfully passing the test and attending more than 90% of classes, and submitting tutorials on time.</p>


